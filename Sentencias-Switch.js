// Sentencias Switch

let nota = 6;

switch (nota) {
     case 5:
          console.log("Execelente")
          break;
     case 4:
          console.log("Bien, puedes mejorar")
          break;
     case 3:
          console.log("es suficiente")
          break;
     case 2:
          console.log("no has aprobado por poco")
          break;
     case 1:
          console.log("reprobado, debes estudiar mas")
          break;
     default:
          console.log("error, introduce un numero entre el 1 y 5")
          break;

}